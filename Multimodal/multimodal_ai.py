class TextEncoder(nn.Module):
    def __init__(self, vocab_size, embedding_dim=100, hidden_dim=256, num_layers=2, dropout=0.5):
        super().__init__()
        self.embedding = nn.Embedding(vocab_size, embedding_dim, padding_idx=0)
        self.lstm = nn.LSTM(embedding_dim, hidden_dim, num_layers, batch_first=True, dropout=dropout)
        self.fc = nn.Linear(hidden_dim, hidden_dim)
        self.dropout = nn.Dropout(dropout)
    def forward(self, text):
        embedded = self.embedding(text)
        lstm_out, (h, c) = self.lstm(embedded)
        output = self.fc(self.dropout(h[-1]))
        return output

class EarlyFusion(nn.Module):
    def __init__(self, num_classes, vocab_size):
        super().__init__()
        self.image_encoder = ImageEncoder()
        self.text_encoder = TextEncoder(vocab_size)
        self.classifier = nn.Sequential(
            nn.Linear(2048 + 256, 256),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(256, num_classes)
        )
    def forward(self, image, text):
        img_feat = self.image_encoder(image)
        txt_feat = self.text_encoder(text)
        combined = torch.cat((img_feat, txt_feat), dim=1)
        return self.classifier(combined)

class LateFusion(nn.Module):
    def __init__(self, num_classes, vocab_size):
        super().__init__()
        self.image_encoder = ImageEncoder()
        self.text_encoder = TextEncoder(vocab_size)
        self.img_classifier = nn.Linear(2048, num_classes)
        self.txt_classifier = nn.Linear(256, num_classes)
    def forward(self, image, text):
        img_feat = self.image_encoder(image)
        txt_feat = self.text_encoder(text)
        img_pred = self.img_classifier(img_feat)
        txt_pred = self.txt_classifier(txt_feat)
        return (img_pred + txt_pred) / 2

