# --- Prediction Function ---
def vqa_predict(image_path, question):
    """Generates an answer for a question about an image using the VQA model."""
    try:
        image = Image.open(image_path).convert("RGB")
    except FileNotFoundError:
        print(f"Error: Image not found at {image_path}. Skipping.")
        return None
    except Exception as e:
        print(f"Error loading image {image_path}: {e}. Skipping.")
        return None

    # Processor handles both image and text inputs
    inputs = processor(image, question, return_tensors="pt")

    with torch.no_grad():
        outputs = model(**inputs)

    # Get the index of the highest logit
    logits = outputs.logits
    idx = logits.argmax(-1).item()

    # Map the index to the predicted answer string
    answer = model.config.id2label[idx]
    return answer.lower()

