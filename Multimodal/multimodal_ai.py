# --- Functions for Metrics ---
def precision_at_k(pred_indices, relevance, k):
    """Fraction of relevant images in the top-K retrieved results."""
    if k == 0 or len(relevance) == 0: return 0.0
    k_indices = pred_indices[:k]
    return relevance[k_indices].sum() / k

def recall_at_k(pred_indices, relevance, k):
    """Fraction of all relevant images retrieved within the top-K results."""
    total_rel = relevance.sum()
    if total_rel == 0: return 0.0
    k_indices = pred_indices[:k]
    return relevance[k_indices].sum() / total_rel

def mean_average_precision(pred_indices, relevance, k):
    """Computes Mean Average Precision (mAP) for a single query (Average Precision)."""
    total_rel = relevance.sum()
    if total_rel == 0: return 0.0

    score = 0.0
    cum_rel = 0
    for i in range(k):
        if relevance[pred_indices[i]] == 1:
            cum_rel += 1
            score += cum_rel / (i + 1)
    return score / total_rel

