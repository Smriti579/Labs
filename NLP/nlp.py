import numpy as np
import tensorflow as tf
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Dense, Embedding, Dropout
from tensorflow.keras.optimizers import Adam
from tensorflow.keras.regularizers import l2
from sklearn.metrics import classification_report

# Sample parsed data (simplified for example purposes)
tree_data = [
    {'sentence': 'I love programming', 'label': 1, 'tree': ['I', ['love', 'programming']]},
    {'sentence': 'I dislike bugs', 'label': 0, 'tree': ['I', ['dislike', 'bugs']]},
    {'sentence': 'Python is great', 'label': 1, 'tree': ['Python', ['is', 'great']]},
    {'sentence': 'I hate errors', 'label': 0, 'tree': ['I', ['hate', 'errors']]}
]

# Word Embeddings (Random Initialization)
vocab = {'I': 0, 'love': 1, 'programming': 2, 'dislike': 3, 'bugs': 4, 'Python': 5, 'is': 6, 'great': 7, 'hate': 8, 'errors': 9}
vocab_size = len(vocab)
embedding_dim = 50
embedding_matrix = np.random.rand(vocab_size, embedding_dim)

# Recursive Composition Layer (RNTN Layer)
class RNTNLayer(tf.keras.layers.Layer):
    def __init__(self, embedding_dim, **kwargs):
        super().__init__(**kwargs)
        self.embedding_dim = embedding_dim
        self.W = self.add_weight(
            shape=(embedding_dim * 2, embedding_dim * 2, embedding_dim),
            initializer='random_normal', name='W')
        self.V = self.add_weight(
            shape=(embedding_dim * 2, embedding_dim),
            initializer='random_normal', name='V')
        self.b = self.add_weight(
            shape=(embedding_dim,), initializer='zeros', name='b')

    def call(self, left, right):
        concat = tf.concat([left, right], axis=-1)  # Shape: (batch, embedding_dim*2)
        tensor_prod = tf.einsum('bi,ijk,bj->bk', concat, self.W, concat)  # Shape: (batch, embedding_dim)
        linear = tf.matmul(concat, self.V)  # Shape: (batch, embedding_dim)
        return tf.nn.tanh(tensor_prod + linear + self.b)

# Fine-tuned RNTN model with extra hidden layer and dropout
class RNTNModel(Model):
    def __init__(self, vocab_size, embedding_dim, embedding_matrix, dropout_rate=0.5, **kwargs):
        super().__init__(**kwargs)
        self.embedding = Embedding(input_dim=vocab_size, output_dim=embedding_dim,
                                   weights=[embedding_matrix], trainable=True)
        self.rntn = RNTNLayer(embedding_dim)
        # Extra dense hidden layer with L2 regularization
        self.hidden = Dense(32, activation='relu', kernel_regularizer=l2(0.01))
        # Dropout layer to reduce overfitting
        self.dropout = Dropout(dropout_rate)
        self.output_dense = Dense(1, activation='sigmoid')

    def compose_tree(self, tree):
        # If the node is a leaf (a word), return its embedding.
        if isinstance(tree, str):
            word_index = vocab.get(tree)
            if word_index is None:
                raise ValueError(f"Word '{tree}' not found in vocabulary.")
            return self.embedding(tf.constant([word_index]))  # Shape: (1, embedding_dim)
        # Otherwise, assume tree is a pair [left, right]
        left, right = tree
        left_emb = self.compose_tree(left)
        right_emb = self.compose_tree(right)
        return self.rntn(left_emb, right_emb)

    def call(self, tree):
        composed = self.compose_tree(tree)
        hidden_out = self.hidden(composed)
        dropped = self.dropout(hidden_out)
        return self.output_dense(dropped)

    def predict_tree(self, tree):
        return self.call(tree)

# Prepare training data
X_train = [item['tree'] for item in tree_data]
y_train = np.array([item['label'] for item in tree_data]).reshape(-1, 1)

# Instantiate the model and optimizer (with a lower learning rate for fine-tuning)
model = RNTNModel(vocab_size, embedding_dim, embedding_matrix, dropout_rate=0.5)
optimizer = Adam(learning_rate=0.001)

# Training loop (tree-by-tree)
epochs = 100
for epoch in range(epochs):
    losses = []
    for i, tree in enumerate(X_train):
        with tf.GradientTape() as tape:
            prediction = model.predict_tree(tree)  # Shape: (1, 1)
            loss = tf.keras.losses.binary_crossentropy(y_train[i:i+1], prediction)
        grads = tape.gradient(loss, model.trainable_variables)
        optimizer.apply_gradients(zip(grads, model.trainable_variables))
        losses.append(loss.numpy())
    if (epoch + 1) % 10 == 0:
        print(f'Epoch {epoch+1}, Loss: {np.mean(losses):.4f}')

# Evaluate the model on training data
y_pred = []
for tree in X_train:
    pred = model.predict_tree(tree).numpy()
    y_pred.append(int(pred > 0.5))
print(classification_report(y_train, y_pred))

