# Preprocess text and generate vocabulary
def preprocess(text):
    text = text.lower().split()
    vocab = set(text)
    word2idx = {word: idx for idx, word in enumerate(vocab)}
    idx2word = {idx: word for word, idx in word2idx.items()}
    return text, word2idx, idx2word
# Generate training data for Word2Vec (skip-gram)
def generate_training_data(text, word2idx, window_size=2):
    data = []
    for i, word in enumerate(text):
        target_word_idx = word2idx[word]
        context_indices = list(range(max(0, i - window_size), i)) + list(range(i + 1, min(len(text), i + window_size + 1)))
        for context_idx in context_indices:
            context_word_idx = word2idx[text[context_idx]]
            data.append((target_word_idx, context_word_idx))
    return np.array(data)
# Initialize the model
def initialize_model(vocab_size, embedding_size):
    W1 = np.random.randn(vocab_size, embedding_size)
    W2 = np.random.randn(embedding_size, vocab_size)
    return W1, W2
# Softmax function
def softmax(x):
    exp_x = np.exp(x - np.max(x))
    return exp_x / exp_x.sum(axis=0)
# Train the Word2Vec model
def train_word2vec(text, vocab_size, word2idx, embedding_size=10, learning_rate=0.001, epochs=5000):
    W1, W2 = initialize_model(vocab_size, embedding_size)
    data = generate_training_data(text, word2idx)
    for epoch in range(epochs):
        loss = 0
        for target_idx, context_idx in data:
            h = W1[target_idx]
            u = np.dot(W2.T, h)
            y_pred = softmax(u)
            y_true = np.zeros(vocab_size)
            y_true[context_idx] = 1
            error = y_pred - y_true
            W2 -= learning_rate * np.outer(h, error)
            W1[target_idx] -= learning_rate * np.dot(W2, error)
            loss += -np.log(y_pred[context_idx])
        # Log loss every 100 epochs
        if epoch % 100 == 0:
            print(f"Epoch {epoch}, Loss: {loss}")
    return W1, W2
# Get word embeddings from trained model
def get_word_embeddings(W1, idx2word):
    word_embeddings = {}
    for idx, word in idx2word.items():
        word_embeddings[word] = W1[idx]
    return word_embeddings
